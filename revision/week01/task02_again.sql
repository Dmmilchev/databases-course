--Напишете заявка, която извежда номер на модел, честота и размер на диска за тези компютри с DVD 12x или 16x и цена по-малка от 2000 долара
select model, speed, hd 
from pc
where (cd = '12x' or cd = '16x') and price <= 20000

--Нека рейтингът на един лаптоп се определя по следната формула: честота на процесора + размер на RAM паметта + 10*размер на екрана. Да се изведат кодовете,
--моделите и рейтингите на всички лаптопи.
select code, model, (speed + hd + 10*screen) as rating
from laptop

--Резултатът да бъде подреден така, че първо да бъдат лаптопите с най-висок рейтинг, а продукти с еднакъв рейтинг да бъдат подредени по код.
select code, model, (speed + hd + 10*screen) as rating
from laptop
order by rating, code desc